package org.h2.faststore;

import java.lang.reflect.Array;

public class Util {

    public static String array2String(Object[] a, int size) {
        if (a == null)
            return "null";

        int iMax =  size - 1;
        if (iMax == -1)
            return "[]";

        StringBuilder b = new StringBuilder();
        b.append('[');
        for (int i = 0; ; i++) {
            b.append(String.valueOf(a[i]));
            if (i == iMax)
                return b.append(']').toString();
            b.append(", ");
        }
    }

}
